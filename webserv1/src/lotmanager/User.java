package lotmanager;

public class User<String> {
		private int id, phone;
		private String username, pass, type, name, address;
		public User(String username, String pass, String type, String name, String address, int phone){
			this.username = username;
			this.pass = pass;
			this.type = type;
			this.name = name;
			this.address = address;
			this.phone = phone;
		}
		
		public User(int id, String username, String pass, String type, String name, String address, int phone){
			this.id = id;
			this.username = username;
			this.pass = pass;
			this.type = type;
			this.name = name;
			this.address = address;
			this.phone = phone;
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public int getPhone() {
			return phone;
		}
		public void setPhone(int phone) {
			this.phone = phone;
		}
		public String getUsername() {
			return username;
		}
		public void setUsername(String username) {
			this.username = username;
		}
		public String getPass() {
			return pass;
		}
		public void setPass(String pass) {
			this.pass = pass;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
}
